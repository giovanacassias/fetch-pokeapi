export class Pokemon{
    constructor(name, ID, sprite, type1, type2, attack, defense, spAttack, spDefense){
        this.name = name;
        this.ID = ID;
        this.sprite = sprite;
        this.type1 = type1;
        this.type2 = type2;
        this.attack = attack;
        this.defense = defense;
        this.spAttack = spAttack;
        this.spDefense = spDefense;
    }
}